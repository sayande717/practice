course = 'Python for Beginners'
#Iterates from the beginning
print(course[1])
#Iterates fromt the end
print(course[-2])
#Printing a range
#Index: 0 to 2 (excluding 3)
print(course[0:3])
#Index: 0 till the end of the string
print(course[1:])
#Index: From the start till 4 (exluding 5)
print(course[:5])