course = 'Python for Beginners'
#Finding length
print(len(course))
#Print the string with all characters converted to upper case.
print(course.upper())
#Does string end with 's'?
print(course.endswith('s'))
#Find the index of element 'f' in the string
print(course.find('f'))
#Does 'Python' exist in the string?
print('Python' in course)
#Replace an element / multiple elements with another one.
print(course.replace('Beginners','Absolute Beginners'))
